%统计某个元胞周围的不同状态的邻居个数
function [neighbors0,neighbors1]=neighbors_fun(state)
%state矩阵102 X 102 neighbors邻居矩阵100 X 100
%通过矩阵平移找到相应的邻居个数
%
%统计死状态的邻居
neighbors0=(state(1:end-2,1:end-2)==0)+...
           (state(1:end-2,2:end-1)==0)+...
		   (state(1:end-2,3:end-0)==0)+...
		   (state(2:end-1,1:end-2)==0)+... %将自己
		   (state(2:end-1,3:end-0)==0)+...
		   (state(3:end,1:end-2)==0)+...
		   (state(3:end,2:end-1)==0)+...
		   (state(3:end,3:end-0)==0);
%统计生状态的邻居		   
neighbors1=(state(1:end-2,1:end-2)==1)+...
           (state(1:end-2,2:end-1)==1)+...
		   (state(1:end-2,3:end-0)==1)+...
		   (state(2:end-1,1:end-2)==1)+...
		   (state(2:end-1,3:end-0)==1)+...
		   (state(3:end,1:end-2)==1)+...
		   (state(3:end,2:end-1)==1)+...
		   (state(3:end,3:end-0)==1);
end